# ---------------------------------------------------------------------------


def test_e2e_6_build_fails_session_preserved():
    """E2E-6: build_workflow returns error → LLM produces friendly error message, session preserved."""
    llm_responses = [
        _tool_call("build_workflow", {"user_request": "complex impossible workflow"}),
        _text("I tried to build the workflow but it timed out. Could you simplify the request?"),
    ]

    tool_results = [
        {
            "ok": False,
            "status": "error",
            "error_category": "building_timeout",
            "error_message": "building_timeout: LLM exceeded 180s",
        },
    ]

    llm = _MockLLM(llm_responses)
    p_llm, p_tools = _build_patches(llm, tool_results)

    with p_llm, p_tools:
        r1 = dispatch_chat_turn(None, "自動化一個複雜的工作流程")

    # Status reflects the tool's error
    assert r1.status == "error"
    assert "timeout" in r1.assistant_text.lower() or "tried" in r1.assistant_text.lower()
    # error_message should be present
    assert r1.error_message is not None

    # Session must still exist — user can continue chatting
    sess = get_session_store().get(r1.session_id)
    assert sess is not None

    # Follow-up chat in same session should work fine
    llm_responses_2 = [_text("Sure, let's try a simpler approach.")]
    llm2 = _MockLLM(llm_responses_2)
    p_llm2, p_tools2 = _build_patches(llm2, [])

    with p_llm2, p_tools2:
        r2 = dispatch_chat_turn(r1.session_id, "Let me simplify")
    assert r2.status == "chat"
    assert r2.session_id == r1.session_id


