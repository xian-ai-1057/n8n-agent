def test_e2e_history_accumulates_across_turns():
    """Sessions accumulate history across multiple chat turns."""
    llm_responses = [
        _text("turn 1 response"),
        _text("turn 2 response"),
        _text("turn 3 response"),
    ]

    llm = _MockLLM(llm_responses)
    p_llm, p_tools = _build_patches(llm, [])

    with p_llm, p_tools:
        r1 = dispatch_chat_turn(None, "message one")
        sid = r1.session_id
        dispatch_chat_turn(sid, "message two")
        dispatch_chat_turn(sid, "message three")

    sess = get_session_store().get(sid)
    assert sess is not None
    # 3 user messages + 3 assistant replies = 6 history entries minimum
    user_msgs = [m for m in sess.history if m.get("role") == "user"]
    asst_msgs = [m for m in sess.history if m.get("role") == "assistant"]
    assert len(user_msgs) == 3
    assert len(asst_msgs) == 3


