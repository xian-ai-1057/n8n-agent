def test_e2e_hitl_flags_cleared_after_deploy():
    """After a successful deployment, awaiting_plan_approval is False and no pending summary."""
    plan = [_step("s1")]

    llm_responses = [
        _tool_call("build_workflow", {"user_request": "build X"}),
        _text("Plan ready. Confirm?"),
        _tool_call("confirm_plan", {"approved": True}),
        _text("Deployed at https://n8n/w/7"),
    ]

    tool_results = [
        {
            "ok": True,
            "status": "awaiting_plan_approval",
            "plan_summary": "step 1 summary",
            "plan": [s.model_dump(mode="json") for s in plan],
        },
        {
            "ok": True,
            "status": "deployed",
            "workflow_url": "https://n8n/w/7",
            "workflow_id": "wf7",
        },
    ]

    llm = _MockLLM(llm_responses)
    p_llm, p_tools = _build_patches(llm, tool_results)

