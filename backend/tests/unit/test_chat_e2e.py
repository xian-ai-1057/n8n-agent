# C1-9:CHAT-DISP-01 / CHAT-TOOL-01/02 / CHAT-API-01 — E2E scenarios

"""End-to-end tests for the chat-first + HITL pipeline using FastAPI TestClient
with fully mocked LLM and graph helpers.

Covers the 6 E2E scenarios from docs/L1-components/C1-9_Chat_Layer.md §E2E
Test Coverage and the test plan generated by /plan-eng-review:

  E2E-1 — Golden path: chitchat → build → HITL confirm → deploy → workflow_url
  E2E-2 — Plan edit path: build → HITL → edit step → resume → deploy
  E2E-3 — Plan reject path: build → HITL → reject → session preserved, can chat
  E2E-4 — Keyword fast-path: keyword in first turn → build called, no extra clarification
  E2E-5 — Clarification convergence: 3 ambiguous turns → LLM never calls tool
  E2E-6 — Error recovery: build fails (graph error) → friendly message, session preserved

All LLM calls are replaced by AIMessage stubs; graph helpers
(run_graph_until_interrupt, resume_graph_with_confirmation) are mocked so no
LangGraph or OpenAI endpoint is needed.

Strategy
--------
We exercise the dispatcher directly (not via the HTTP layer) to avoid the
FastAPI / asyncio overhead. The HTTP-layer mapping tests are in
test_routes_chat.py. E2E tests here validate the full multi-turn state
machine: session persistence, history accumulation, HITL flag transitions.
"""

