def _step(step_id: str, intent=StepIntent.ACTION) -> StepPlan:
    return StepPlan(
        step_id=step_id,
        description=f"description for {step_id}",
        intent=intent,
        candidate_node_types=["n8n-nodes-base.httpRequest"],
        reason="test",
    )


def _agent_state(**kwargs: Any) -> AgentState:
    defaults: dict[str, Any] = {"user_message": "test"}
    defaults.update(kwargs)
    return AgentState(**defaults)


def _text(content: str) -> AIMessage:
    return AIMessage(content=content)


def _tool_call(name: str, args: dict, tool_id: str = "call_1") -> AIMessage:
    return AIMessage(
        content="",
        tool_calls=[{"id": tool_id, "name": name, "args": args}],
    )


class _MockLLM:
    """Configurable LLM stub for a sequence of pre-programmed responses."""

    def __init__(self, responses: list[AIMessage]):
        self._responses = list(responses)
        self._call_count = 0
        self._bound_responses: list[AIMessage] | None = None

    def bind_tools(self, tools, **kwargs) -> "_MockLLM":
        """Return a bound variant that delegates back to this object."""
        bound = _MockLLM(self._responses[:])
        bound._responses = self._responses  # shared reference
        bound._call_count = 0
        # Save reference to tool_choice to decide behaviour
        bound._tool_choice = kwargs.get("tool_choice")
        return bound

    def invoke(self, messages) -> AIMessage:
        if self._responses:
            resp = self._responses.pop(0)
            self._call_count += 1
            return resp
        # Fallback: return empty text
        return _text("(mock exhausted)")


def _build_patches(llm: _MockLLM, tool_results: list[dict[str, Any]]):
    """Return context managers that wire the mock LLM and tool results."""
    tool_result_iter = iter(tool_results)

    def _make_tools(session_id, **kwargs):
        build_tool = MagicMock()
        build_tool.name = "build_workflow"
        confirm_tool = MagicMock()
        confirm_tool.name = "confirm_plan"

        def _invoke_build(args):
            return next(tool_result_iter, {
                "ok": False, "status": "error", "error_message": "exhausted"
            })

        def _invoke_confirm(args):
            return next(tool_result_iter, {
                "ok": False, "status": "error", "error_message": "exhausted"
            })

        build_tool.invoke.side_effect = _invoke_build
        confirm_tool.invoke.side_effect = _invoke_confirm
        return [build_tool, confirm_tool]

    p_llm = patch("app.chat.dispatcher._make_chat_llm", return_value=llm)
    p_tools = patch("app.chat.dispatcher.make_chat_tools", side_effect=_make_tools)
    return p_llm, p_tools


