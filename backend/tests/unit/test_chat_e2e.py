def test_e2e_5_ambiguous_3_turns_no_tool_call():
    """E2E-5: 3 turns of ambiguous requests → LLM asks clarifying questions, never calls tool."""
    # All 3 user-facing LLM responses + potential humanise calls — none use tool_calls
    llm_responses = [
        _text("I'd be happy to help! Could you tell me what you want to automate?"),
        _text("Could you clarify the trigger — should this run on a schedule or a webhook?"),
        _text("And what should happen with the data — where should it be sent or stored?"),
    ]

    llm = _MockLLM(llm_responses)
    p_llm, p_tools = _build_patches(llm, [])  # no tool results needed

    with p_llm, p_tools:
        r1 = dispatch_chat_turn(None, "我想做點自動化")
        assert r1.status == "chat"
        assert r1.tool_calls == []
        sid = r1.session_id

        r2 = dispatch_chat_turn(sid, "就是一些流程之類的")
        assert r2.status == "chat"
        assert r2.tool_calls == []

        r3 = dispatch_chat_turn(sid, "自動化之類的")
        assert r3.status == "chat"
        assert r3.tool_calls == []

    # All 3 turns should be in history
    sess = get_session_store().get(sid)
    assert sess is not None
    # Each turn adds user + assistant = 2 messages, so 3 turns = 6 messages
    assert len(sess.history) >= 4


