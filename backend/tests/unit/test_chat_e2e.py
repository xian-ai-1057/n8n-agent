# E2E-1 — Golden path: chitchat → clarification → build → confirm → deploy
# ---------------------------------------------------------------------------


def test_e2e_1_golden_path_chitchat_build_confirm_deploy():
    """E2E-1: Multi-turn chitchat, then build_workflow + HITL confirm → deployed."""
    plan = [
        _step("step_1", StepIntent.TRIGGER),
        _step("step_2", StepIntent.ACTION),
    ]
    plan_state = _agent_state(plan=plan)
    deploy_state = _agent_state(
        plan=plan,
        workflow_url="https://n8n/w/42",
        workflow_id="wf42",
    )

    # Sequence: chitchat → clarification question → build → confirm → friendly deploy
    llm_responses = [
        _text("Hi! How can I help you today?"),          # turn 1: chitchat
        _text("What would you like to automate?"),        # turn 2: clarification
        _tool_call("build_workflow", {"user_request": "fetch github stars hourly to sheet"}),  # turn 3: decision
        _text("Great! Here is the plan: 1. Trigger every hour 2. Fetch data. Confirm?"),       # turn 3: humanise
        _tool_call("confirm_plan", {"approved": True}),  # turn 4: user confirms
        _text("Deployed! Your workflow is live at https://n8n/w/42"),  # turn 4: humanise
    ]

    tool_results = [
        {
            "ok": True,
            "status": "awaiting_plan_approval",
            "plan_summary": "1. trigger hourly\n2. fetch github",
            "plan": [s.model_dump(mode="json") for s in plan],
            "session_id": "e2e_sess_001",
        },
        {
            "ok": True,
            "status": "deployed",
            "workflow_url": "https://n8n/w/42",
            "workflow_id": "wf42",
        },
    ]

    llm = _MockLLM(llm_responses)
    p_llm, p_tools = _build_patches(llm, tool_results)

    with p_llm, p_tools:
        # Turn 1: pure chitchat
        r1 = dispatch_chat_turn(None, "Hello there!")
        assert r1.status == "chat"
        assert r1.workflow_url is None
        sid = r1.session_id

        # Turn 2: clarification question from the model
        r2 = dispatch_chat_turn(sid, "I want to automate something")
        assert r2.status == "chat"

        # Turn 3: clear request → LLM calls build_workflow
        r3 = dispatch_chat_turn(sid, "Every hour fetch github stars and save to Google Sheet")
        assert r3.status == "awaiting_plan_approval"
        assert r3.workflow_url is None
        # Session should be marked as awaiting
        sess = get_session_store().get(sid)
        assert sess is not None
        assert sess.awaiting_plan_approval is True

        # Turn 4: user confirms → LLM calls confirm_plan(approved=True) → deployed
        r4 = dispatch_chat_turn(sid, "確認")
        assert r4.status == "deployed"
        assert r4.workflow_url == "https://n8n/w/42"
        assert r4.workflow_id == "wf42"

    # Session history should span all 4 turns
    sess = get_session_store().get(sid)
    assert sess is not None
    assert len(sess.history) >= 4  # at least user messages


