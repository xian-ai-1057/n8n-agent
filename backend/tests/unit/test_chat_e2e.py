def test_e2e_3_plan_reject_session_preserved():
    """E2E-3: User rejects the plan; session keeps history and can continue chatting."""
    plan = [_step("step_1", StepIntent.TRIGGER), _step("step_2", StepIntent.ACTION)]

    llm_responses = [
        _tool_call("build_workflow", {"user_request": "build automation"}),
        _text("Here's the plan. Do you want to proceed?"),
        # User rejects
        _tool_call("confirm_plan", {"approved": False, "feedback": "wrong approach"}),
        _text("No problem, I've cancelled the plan. What would you like to do instead?"),
        # Continued chat after rejection
        _text("Sure, let me know your revised requirements."),
    ]

    tool_results = [
        {
            "ok": True,
            "status": "awaiting_plan_approval",
            "plan_summary": "1. trigger\n2. action",
            "plan": [s.model_dump(mode="json") for s in plan],
        },
        {
            "ok": True,
            "status": "rejected",
            "message": "plan rejected; you can refine the request and try again.",
        },
    ]

    llm = _MockLLM(llm_responses)
    p_llm, p_tools = _build_patches(llm, tool_results)

    with p_llm, p_tools:
        # Turn 1: build
        r1 = dispatch_chat_turn(None, "Build me an automation workflow")
        assert r1.status == "awaiting_plan_approval"
        sid = r1.session_id

        # Turn 2: reject
        r2 = dispatch_chat_turn(sid, "不要了, wrong approach")
        assert r2.status == "rejected"
        # Session must still exist
        sess = get_session_store().get(sid)
        assert sess is not None
        # After rejection, awaiting_plan_approval should be cleared
        assert sess.awaiting_plan_approval is False

        # Turn 3: continued chat after rejection — session is usable
        r3 = dispatch_chat_turn(sid, "I'll think about what I need")
        assert r3.status == "chat"
        assert r3.session_id == sid


